// if文
let score = 80;

if (score >= 70) {
    console.log("合格");
}

// if文とelse
let score2 = 70;

if (score2 >= 70) {
    console.log("合格");
} else {
    console.log("不合格");
}

// if文とelse if
let score3 = 85;

if (score3 >= 90) {
    console.log("優秀");
} else if (score3 >= 70) {
    console.log("合格");
} else {
    console.log("不合格");
}